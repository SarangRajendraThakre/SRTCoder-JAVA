package Inheritance;

public class P_1_SimpleCalc {

    // Addition
    public int add(int a, int b) {
        return a + b;
    }

    // Subtraction
    public int sub(int a, int b) {
        return a - b;
    }

    // Overloaded for double
    public double add(double a, double b) {
        return a + b;
    }

    public double sub(double a, double b) {
        return a - b;
    }
}
