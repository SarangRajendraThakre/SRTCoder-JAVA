
public class P171_AutoBoxing {

    public static void main(String[] args) {

        int num = 123;
        Integer obj1 = num;

        boolean b = false;
        Boolean obj2 = b;

        float c = 1.2f;
        Float obj3 = c;

        System.out.println(obj1);
        System.out.println(obj2);
        System.out.println(obj3);
    }
}
