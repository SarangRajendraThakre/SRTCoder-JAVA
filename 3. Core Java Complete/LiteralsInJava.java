public class LiteralsInJava {

    public static void main (String args[])
    {
        int a = 10_00_00_000;
        System.out.println(a);


        int b = 0x00a;
        System.out.println(b);


        double c = 11e23;
        System.out.println(c);

        double d = 56;
        System.out.println(d);

        char ch = 'a';
        ch++;
        System.out.println(ch);
        
    }
    
}
