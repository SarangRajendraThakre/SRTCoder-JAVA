public class P13_TypeCasting_Narrowing {

    public static void main(String args[]){

        int a = 34 ;
        // byte b = a ;         

        byte b = (byte) a ;
        System.out.println(b);


        float f = 3.5f;
        int i = (int) f;
        System.out.println(i);
        


    }
    
}
